public class RightInfo {
    public String applyName;//申请人
    public String sqName;//授权人
    public String equipmentID;//设备ID
    public String sqNumber;//授权次数
    public String sqReason;//授权原因
    public String sqResult;//授权结果
    public String sqCode;//授权编号
    public String usestate;//申请状态
    public String sqtime;//授权时间

    public String getApplyName() {
        return applyName;
    }

    public void setApplyName(String applyName) {
        this.applyName = applyName;
    }

    public String getSqName() {
        return sqName;
    }

    public void setSqName(String sqName) {
        this.sqName = sqName;
    }

    public String getEquipmentID() {
        return equipmentID;
    }

    public void setEquipmentID(String equipmentID) {
        this.equipmentID = equipmentID;
    }

    public String getSqNumber() {
        return sqNumber;
    }

    public void setSqNumber(String sqNumber) {
        this.sqNumber = sqNumber;
    }

    public String getSqReason() {
        return sqReason;
    }

    public void setSqReason(String sqReason) {
        this.sqReason = sqReason;
    }

    public String getSqResult() {
        return sqResult;
    }

    public void setSqResult(String sqResult) {
        this.sqResult = sqResult;
    }

    public String getSqCode() {
        return sqCode;
    }

    public void setSqCode(String sqCode) {
        this.sqCode = sqCode;
    }

    public String getUsestate() {
        return usestate;
    }

    public void setUsestate(String usestate) {
        this.usestate = usestate;
    }

    public String getSqtime() {
        return sqtime;
    }

    public void setSqtime(String sqtime) {
        this.sqtime = sqtime;
    }
}
